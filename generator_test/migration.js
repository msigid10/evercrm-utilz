import http from "k6/http";
import { describe, expect } from "https://jslib.k6.io/k6chaijs/4.3.4.1/index.js";
import { htmlReport } from 'https://raw.githubusercontent.com/depapp/k6-reporter-bootstrap/main/dist/bundle.js';

const BASE_URL = "http://evercrm.dev.internal";

export default function() {
  describe("/v1/migration/message-status-to-customer", () => {

    let url = BASE_URL + `/v1/migration/message-status-to-customer`;
    let body = {"limit":0,"merchantCode":"string","offset":0};
    let params = {headers: {"Accept":"application/json","Authorization":"<Internal Secret>","Migration-Secret":"<Migration Secret>","X-Client-Application":"Client Application (ex: appsmith)"}};
    let response = http.post(url, JSON.stringify(body), params);

    // print API response
    console.log(response.body);

    // test assertion section
    expect(response.status, 'API status code').to.equal(200);
  });

}

export function handleSummary(data) {
  console.log("Preparing the end-of-test summary...");
  return {
    "test-report.html": htmlReport(data, {
      title: "Migration - Test Report",
      template: "bootstrap",
    }),
  };
}
